<?php

// switch-case --> percabangan

$angkaHari=9;

switch ($angkaHari){
    case 1:
        echo "Ahad";
        break;
    case 2:
        echo "Senin";
        break;
    case 3:
        echo "Selasa";
        break;
    case 4:
        echo "Rabu";
        break;
    case 5:
        echo "Kamis";
        break;
    case 6:
        echo "Jumat";
        break;
    case 7:
        echo "Sabtu";
        break;
    default:
        echo "Angka tidak terdaftar";
}
